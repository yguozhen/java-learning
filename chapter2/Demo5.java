package com.javalearning.chapter2;

public class Demo5 {
	public static void main(String[] args) {
		//test > < >= <=
		System.out.println("2 > 3\t"+ (2>3));
		System.out.println("2 < 3\t"+ (2<3));
		System.out.println("2 >= 3\t"+ (2>=3));
		System.out.println("2 <= 3\t"+ (2<=3));
		System.out.println("2 == 3\t"+ (2==3));
		System.out.println("2 != 3\t"+ (2!=3));
	}

}
